package com.javacode.qspider_assignment.day2;

class PersonalByteData {
    public static void main(String[] args) {
        byte age = 21;
        byte noOfChildren = 0;
        byte myRating = 9;
        byte weight = 82;
        byte nameLength = 8;
        System.out.println("My age is " + age + " years");
        System.out.println("No. of children: " + noOfChildren);
        System.out.println("I rate myself as " + myRating + "/10");
        System.out.println("My weight is " + weight + " kg");
        System.out.println("Length of my name is " + nameLength + " letters");
    }
}
