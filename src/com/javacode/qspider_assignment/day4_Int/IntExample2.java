package com.javacode.qspider_assignment.day4_Int;

public class IntExample2 {
    public static void main(String[] args){
        int hairGrowth = 10000;
        int wormsUnderground = 1000000;
        int englishWords = 8000000;
        int insectPerHuman = 1400000000;
        System.out.println("People have about " + hairGrowth + " hairs growing on their hairs.");
        System.out.println("One acre of land may have from 50,000 to " + wormsUnderground + " worms underground.");
        System.out.println("There are about " + englishWords + " words in English language.");
        System.out.println("For every human alive on Earth, there are about " + insectPerHuman + " insects.");
    }
}
